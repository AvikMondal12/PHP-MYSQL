<?php
$varibale =10;
echo $varibale;
echo "<br>";

//increment operator[before print]
echo ++$varibale;
echo "<br>";
//decrement operator[before print]
echo --$varibale;
echo "<br>";
//increment operator[after print]
$varibale++;
echo $varibale;
echo "<br>";
//decrement operator[after print]
$varibale--;
echo $varibale;

?>